-- ============================================================
-- PROYECTO ACCESOS V2 - ETAPA 1
-- Catalogos, nodos, administradores, accesos, personal y SCTR.
--
-- Diseñado para ejecutarse en un proyecto Supabase NUEVO Y VACIO.
-- No elimina tablas ni datos existentes.
-- ============================================================

BEGIN;

-- ============================================================
-- 1. CATALOGOS
-- ============================================================

CREATE TABLE IF NOT EXISTS public.tipos_documento (
  id INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  nombre TEXT NOT NULL UNIQUE,
  activo BOOLEAN NOT NULL DEFAULT TRUE
);

CREATE TABLE IF NOT EXISTS public.niveles_acceso (
  id INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  nombre TEXT NOT NULL UNIQUE,
  activo BOOLEAN NOT NULL DEFAULT TRUE
);

CREATE TABLE IF NOT EXISTS public.tipos_trabajo (
  id INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  nombre TEXT NOT NULL UNIQUE,
  gestiona_equipos BOOLEAN NOT NULL DEFAULT FALSE,
  activo BOOLEAN NOT NULL DEFAULT TRUE
);

CREATE TABLE IF NOT EXISTS public.empresas (
  id INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  nombre TEXT NOT NULL UNIQUE,
  activo BOOLEAN NOT NULL DEFAULT TRUE
);

CREATE TABLE IF NOT EXISTS public.areas (
  id INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  empresa_id INTEGER NOT NULL
    REFERENCES public.empresas(id) ON DELETE RESTRICT,
  nombre TEXT NOT NULL,
  activo BOOLEAN NOT NULL DEFAULT TRUE,
  CONSTRAINT areas_empresa_nombre_unique UNIQUE (empresa_id, nombre)
);

CREATE TABLE IF NOT EXISTS public.nodos (
  id INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  nombre TEXT NOT NULL UNIQUE,
  requiere_llave BOOLEAN NOT NULL DEFAULT FALSE,
  netbox_site_id INTEGER UNIQUE,
  activo BOOLEAN NOT NULL DEFAULT TRUE
);

-- ============================================================
-- 2. ADMINISTRADORES
-- Solo los usuarios registrados aqui tienen permisos de admin.
-- ============================================================

CREATE TABLE IF NOT EXISTS public.administradores (
  user_id UUID PRIMARY KEY
    REFERENCES auth.users(id) ON DELETE CASCADE,
  nombre TEXT NOT NULL,
  activo BOOLEAN NOT NULL DEFAULT TRUE,
  created_at TIMESTAMPTZ NOT NULL DEFAULT NOW()
);

CREATE OR REPLACE FUNCTION public.es_admin()
RETURNS BOOLEAN
LANGUAGE sql
STABLE
SECURITY DEFINER
SET search_path = public
AS $$
  SELECT EXISTS (
    SELECT 1
    FROM public.administradores a
    WHERE a.user_id = auth.uid()
      AND a.activo = TRUE
  );
$$;

REVOKE ALL ON FUNCTION public.es_admin() FROM PUBLIC;
GRANT EXECUTE ON FUNCTION public.es_admin() TO authenticated;

-- ============================================================
-- 3. SOLICITUDES DE ACCESO
-- El id numerico es interno. codigo_seguimiento es el codigo
-- que se entregara al tecnico para consultar su solicitud.
-- ============================================================

CREATE TABLE IF NOT EXISTS public.accesos (
  id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  codigo_seguimiento UUID NOT NULL DEFAULT gen_random_uuid() UNIQUE,

  nodo_id INTEGER NOT NULL
    REFERENCES public.nodos(id) ON DELETE RESTRICT,
  fecha_ingreso DATE NOT NULL,
  fecha_salida DATE NOT NULL,
  hora_ingreso TIME NOT NULL,
  hora_salida TIME NOT NULL,

  solicitante_nombre TEXT NOT NULL,
  solicitante_ap_paterno TEXT NOT NULL,
  solicitante_ap_materno TEXT NOT NULL,
  solicitante_tipo_doc_id INTEGER NOT NULL
    REFERENCES public.tipos_documento(id) ON DELETE RESTRICT,
  solicitante_num_doc TEXT NOT NULL,
  solicitante_telefono TEXT NOT NULL,
  solicitante_correo TEXT NOT NULL,

  nivel_acceso_id INTEGER NOT NULL
    REFERENCES public.niveles_acceso(id) ON DELETE RESTRICT,
  empresa_id INTEGER NOT NULL
    REFERENCES public.empresas(id) ON DELETE RESTRICT,
  area_responsable_id INTEGER NOT NULL
    REFERENCES public.areas(id) ON DELETE RESTRICT,
  area_apoyo_id INTEGER
    REFERENCES public.areas(id) ON DELETE RESTRICT,
  tipo_trabajo_id INTEGER NOT NULL
    REFERENCES public.tipos_trabajo(id) ON DELETE RESTRICT,
  detalle_trabajo TEXT,

  trabajo_contrata BOOLEAN NOT NULL DEFAULT FALSE,
  nombre_contrata TEXT,
  numero_personal INTEGER NOT NULL,

  estado_aprobacion TEXT NOT NULL DEFAULT 'PENDIENTE',
  estado_acceso TEXT NOT NULL DEFAULT 'PENDIENTE',
  motivo_cancelacion TEXT,
  hora_ingreso_real TIME,
  hora_salida_real TIME,

  aprobado_por UUID REFERENCES auth.users(id) ON DELETE SET NULL,
  fecha_aprobacion TIMESTAMPTZ,
  created_at TIMESTAMPTZ NOT NULL DEFAULT NOW(),
  updated_at TIMESTAMPTZ NOT NULL DEFAULT NOW(),

  CONSTRAINT accesos_numero_personal_check
    CHECK (numero_personal BETWEEN 1 AND 8),
  CONSTRAINT accesos_fechas_check
    CHECK (fecha_salida >= fecha_ingreso),
  CONSTRAINT accesos_horario_check
    CHECK (
      fecha_salida > fecha_ingreso
      OR hora_salida > hora_ingreso
    ),
  CONSTRAINT accesos_contrata_check
    CHECK (
      (trabajo_contrata = FALSE AND nombre_contrata IS NULL)
      OR
      (trabajo_contrata = TRUE AND NULLIF(BTRIM(nombre_contrata), '') IS NOT NULL)
    ),
  CONSTRAINT accesos_estado_aprobacion_check
    CHECK (estado_aprobacion IN ('PENDIENTE', 'APROBADO', 'DENEGADO', 'CANCELADO')),
  CONSTRAINT accesos_estado_acceso_check
    CHECK (estado_acceso IN ('PENDIENTE', 'EN_NODO', 'ATENDIDO', 'CANCELADO')),
  CONSTRAINT accesos_aprobacion_consistente_check
    CHECK (
      (estado_aprobacion = 'PENDIENTE' AND aprobado_por IS NULL AND fecha_aprobacion IS NULL)
      OR
      (estado_aprobacion IN ('APROBADO', 'DENEGADO') AND aprobado_por IS NOT NULL AND fecha_aprobacion IS NOT NULL)
      OR
      (estado_aprobacion = 'CANCELADO')
    )
);

CREATE INDEX IF NOT EXISTS accesos_nodo_idx
  ON public.accesos(nodo_id);
CREATE INDEX IF NOT EXISTS accesos_fecha_ingreso_idx
  ON public.accesos(fecha_ingreso);
CREATE INDEX IF NOT EXISTS accesos_estado_aprobacion_idx
  ON public.accesos(estado_aprobacion);
CREATE INDEX IF NOT EXISTS accesos_tipo_trabajo_idx
  ON public.accesos(tipo_trabajo_id);

-- ============================================================
-- 4. PERSONAL Y DOCUMENTOS
-- Los documentos SCTR se guardaran en un bucket privado. Esta
-- tabla conserva solo la referencia y metadatos del archivo.
-- ============================================================

CREATE TABLE IF NOT EXISTS public.personal_acceso (
  id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  acceso_id BIGINT NOT NULL
    REFERENCES public.accesos(id) ON DELETE CASCADE,
  orden INTEGER NOT NULL,
  nombre TEXT NOT NULL,
  ap_paterno TEXT NOT NULL,
  ap_materno TEXT NOT NULL,
  tipo_doc_id INTEGER NOT NULL
    REFERENCES public.tipos_documento(id) ON DELETE RESTRICT,
  num_doc TEXT NOT NULL,
  telefono TEXT NOT NULL,
  created_at TIMESTAMPTZ NOT NULL DEFAULT NOW(),
  CONSTRAINT personal_acceso_orden_check CHECK (orden BETWEEN 1 AND 8),
  CONSTRAINT personal_acceso_orden_unique UNIQUE (acceso_id, orden),
  CONSTRAINT personal_acceso_documento_unique UNIQUE (acceso_id, num_doc)
);

CREATE INDEX IF NOT EXISTS personal_acceso_acceso_idx
  ON public.personal_acceso(acceso_id);

CREATE TABLE IF NOT EXISTS public.acceso_documentos (
  id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  acceso_id BIGINT NOT NULL
    REFERENCES public.accesos(id) ON DELETE CASCADE,
  storage_path TEXT NOT NULL UNIQUE,
  nombre_original TEXT NOT NULL,
  mime_type TEXT NOT NULL DEFAULT 'application/pdf',
  size_bytes BIGINT NOT NULL,
  created_at TIMESTAMPTZ NOT NULL DEFAULT NOW(),
  CONSTRAINT acceso_documentos_mime_check
    CHECK (mime_type = 'application/pdf'),
  CONSTRAINT acceso_documentos_size_check
    CHECK (size_bytes > 0 AND size_bytes <= 10485760)
);

CREATE INDEX IF NOT EXISTS acceso_documentos_acceso_idx
  ON public.acceso_documentos(acceso_id);

-- ============================================================
-- 5. UPDATED_AT
-- ============================================================

CREATE OR REPLACE FUNCTION public.actualizar_updated_at()
RETURNS TRIGGER
LANGUAGE plpgsql
SET search_path = public
AS $$
BEGIN
  NEW.updated_at = NOW();
  RETURN NEW;
END;
$$;

DROP TRIGGER IF EXISTS accesos_actualizar_updated_at ON public.accesos;
CREATE TRIGGER accesos_actualizar_updated_at
BEFORE UPDATE ON public.accesos
FOR EACH ROW
EXECUTE FUNCTION public.actualizar_updated_at();

-- ============================================================
