ALTER TABLE public.niveles_acceso ENABLE ROW LEVEL SECURITY;
ALTER TABLE public.tipos_trabajo ENABLE ROW LEVEL SECURITY;
ALTER TABLE public.empresas ENABLE ROW LEVEL SECURITY;
ALTER TABLE public.areas ENABLE ROW LEVEL SECURITY;
ALTER TABLE public.nodos ENABLE ROW LEVEL SECURITY;
ALTER TABLE public.administradores ENABLE ROW LEVEL SECURITY;
ALTER TABLE public.accesos ENABLE ROW LEVEL SECURITY;
ALTER TABLE public.personal_acceso ENABLE ROW LEVEL SECURITY;
ALTER TABLE public.acceso_documentos ENABLE ROW LEVEL SECURITY;

